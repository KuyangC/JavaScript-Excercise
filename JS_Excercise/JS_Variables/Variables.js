/*
4 Ways to Declare a JavaScript Variable
    1. Using "var"
    2. Using "let"
    3. Using "const"
    4. Using nothing
*/

/*
Variable are container for storing data (storing data values).
In this ex "x, y, z" are variables, declared with the "var" keyword
*/

//example of var
var x = 5;
var y = 6;
var z = x + y;
console.log(z);

//example of let
let q = 2;
let w = 3;
let e = q + w;
console.log(e)

//example of undeclared variables
a = 4;
s = 5;
d = a + s;
console.log(d)

//example of const
const price1 = 3;
const price2 = 4;
let total = price1 + price2;
console.log(total)

